package labs;

public class DataTypes {

	public static void main(String[] args) {
		boolean on = false;
		System.out.println(on);
		short s = 16;
		System.out.println(s);
		int i = -32;
		System.out.println(i);
		float f = 3897.134F;
		System.out.println(f);
		long l = 12345678999L;
		System.out.println(l);
		char c = 'b';
		System.out.println(c);
		
		
	}

}
