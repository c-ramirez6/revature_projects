package labs;

public class SimpleMath {

	public static void main(String[] args) {
		int x = 5;
		int y = 3;
		int z;
		
		z = x + y;
		System.out.println(z);
		
		z = x - y;
		System.out.println(z);
		
		z = x * y;
		System.out.println(z);
		
		z = x/y;
		System.out.println(z);
		
		z = x % y;
		System.out.println(z);
		
		float f = 3.6F;
		int i = 22;
		double result = f + i;
		System.out.println(result);

	}

}
